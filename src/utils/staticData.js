export const filterCardContentBy = (card_content, filter) => {
  const filterCards = card_content.filter(item => {
    let flag = false
    filter.map(headerIndex => {
      if (parseInt(headerIndex) === item.card_id) {
        flag = true
      }
      return ""
    })
    return flag
  })
  return filterCards
}

