export const getResourceSubtags = (allSubtags, groupSubtags) => {
  const fn = item => {
    for (let i = 0; i < allSubtags.length; i++) {
      if (allSubtags[i].prismicId === item.subtag.id) {
        return allSubtags[i].data
      }
    }
  }
  return groupSubtags.map(fn)
}
